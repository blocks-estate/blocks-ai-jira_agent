import { Router, Request, Response } from 'express';
import { loadDomainData, uuid, getItems, findItem } from '../common/helpers';

const router = Router();
const DOMAIN = 'product-marketing';

// ─── Launch Tracker ───────────────────────────────────────────────────────
router.get('/launch-tracker', (_req: Request, res: Response) => {
  const data = loadDomainData<any>(DOMAIN);
  if (!data) return res.status(404).json({ error: 'Domain data not found' });
  res.json(getItems(data, 'launchTracker'));
});

router.post('/launch-tracker', (_req: Request, res: Response) => {
  const newItem = { id: uuid(), ..._req.body, createdAt: new Date().toISOString() };
  res.status(201).json(newItem);
});

router.patch('/launch-tracker/:id', (_req: Request, res: Response) => {
  const data = loadDomainData<any>(DOMAIN);
  if (!data) return res.status(404).json({ error: 'Domain data not found' });
  const item = findItem(data, 'launchTracker', _req.params.id);
  if (!item) return res.status(404).json({ error: 'Launch tracker item not found' });
  res.json({ ...item, ..._req.body });
});

